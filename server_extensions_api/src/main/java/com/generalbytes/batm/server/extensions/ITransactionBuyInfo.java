package com.generalbytes.batm.server.extensions;

import java.math.BigDecimal;
import java.util.Date;
import java.util.Map;

public interface ITransactionBuyInfo {
    /**
     * @return unique (in server scope) transaction id. It is generated by server.
     */
    String getRemoteTransactionId();

    /**
     * @return transaction id generated locally by terminal to perform request on server.
     * Don't use it if you don't have to. It is used only for time before server assigns remote transaction id to a transaction.
     */
    String getLocalTransactionId();

    /**
     * @return status of the transaction
     * @see ITransactionDetails
     */
    int getStatus();

    /**
     * @return Fiat amount
     */
    BigDecimal getCashAmount();

    /**
     * @return Fiat currency code (USD, EUR etc)
     */
    String getCashCurrency();

    /**
     * @return Cryptocurrency amount
     */
    BigDecimal getCryptoAmount();

    /**
     * @return cryptocurrency code (e.g. BTC, LTC, ...)
     */
    String getCryptoCurrency();

    /**
     * @return destination cryptocurrency address
     */
    String getCryptoAddress();

    /**
     * @return Custom texts that should be written on the ticket. Custom data are usually generated by ITransactionListener
     */
    Map<String, String> getCustomData();

    /**
     * Server time of the transaction
     *
     * @return {@link Date}
     */
    Date getServerTime();

    /**
     * Terminal time of the transaction - calculated from the terminal location timezone (you may have terminals across multiple time zones)
     *
     * @return {@link Date}
     */
    Date getTerminalTime();
}
