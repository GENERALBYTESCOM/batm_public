package com.generalbytes.batm.server.extensions.payment;

import com.generalbytes.batm.server.extensions.IWallet;

import java.math.BigDecimal;
import java.util.List;

/**
 * Payment specification is generated by server before asks @{@link IPaymentSupport } to create @{@link PaymentRequest}
 */
public interface IPaymentRequestSpecification {
    String getCryptoCurrency();
    String getDescription();

    void addOutput(String address, BigDecimal amount);
    List<IPaymentOutput> getOutputs();
    BigDecimal getTotal();

    long getValidInSeconds();
    String getTimeoutRefundAddress();
    void setTimeoutRefundAddress(String newRefundAddress);

    BigDecimal getOptimalMiningFee(BigDecimal feeCalculated, int transactionSize);
    void removeTotalAmountFromOutputs(BigDecimal totalAmountToRemove);
    int getRemoveAfterNumberOfConfirmationsOfOutgoingTransaction();
    int getRemoveAfterNumberOfConfirmationsOfIncomingTransaction();
    boolean isDoNotForward();
    boolean isZeroFixedFee();
    void optimize();

    IWallet getWallet();
}
