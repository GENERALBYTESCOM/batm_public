package com.generalbytes.batm.server.extensions;

public interface IReceiptDetails {

    String CUSTOM_DATA_RECEIPT_LANGUAGE = "receipt.language";
    String CUSTOM_DATA_RECEIPT_CELLPHONE = "receipt.cellphone";
    String CUSTOM_DATA_RECEIPT_EMAIL = "receipt.email";

    /**
     * @return serial number of the terminal where the transaction was created
     */
    String getTerminalSerialNumber();

    /**
     * @return unique (in server scope) transaction id. It is generated by server.
     */
    String getRemoteTransactionId();

    /**
     *
     * @return user cellphone used to send the receipt to
     */
    String getCellphone();

    /**
     * @return user email used to send the receipt to
     */
    String getEmail();

    /**
     * @return user selected language on the terminal
     */
    String getUserLanguage();

}
